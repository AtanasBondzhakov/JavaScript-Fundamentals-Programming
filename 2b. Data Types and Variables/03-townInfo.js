function townInfo(town, population, area) {
    console.log(`Town ${town} has population of ${population} and area ${area} square km.`);
}

townInfo('Sofia', 1286383, 492); //Town Sofia has population of 1286383 and area 492 square km.
townInfo('Plovdiv', 1481353, 512); //Town Plovdiv has population of 1481353 and area 512 square km.